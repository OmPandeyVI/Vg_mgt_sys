    <script>
        const CLIENT_ID = 'YOUR_CLIENT_ID_HERE';
        const API_KEY = 'YOUR_API_KEY_HERE';
        const SCOPES = 'https://www.googleapis.com/auth/calendar.events';

        document.getElementById('authorizeBtn').onclick = handleAuth;
        document.getElementById('createEventBtn').onclick = createEvent;

        function handleAuth() {
            gapi.load('client:auth2', () => {
                gapi.client.init({
                    apiKey: API_KEY,
                    clientId: CLIENT_ID,
                    discoveryDocs: ["https://www.googleapis.com/discovery/v1/apis/calendar/v3/rest"],
                    scope: SCOPES,
                }).then(() => {
                    gapi.auth2.getAuthInstance().signIn();
                });
            });
        }

        function createEvent() {
            const event = {
                summary: 'Demo Reminder Event',
                description: 'Created via JavaScript and Google Calendar API',
                start: {
                    dateTime: '2025-06-03T10:00:00+05:30',
                    timeZone: 'Asia/Kolkata',
                },
                end: {
                    dateTime: '2025-06-03T11:00:00+05:30',
                    timeZone: 'Asia/Kolkata',
                },
                reminders: {
                    useDefault: false,
                    overrides: [{
                            method: 'email',
                            minutes: 60
                        },
                        {
                            method: 'popup',
                            minutes: 10
                        },
                    ],
                },
            };

            gapi.client.calendar.events.insert({
                calendarId: 'primary',
                resource: event,
            }).then(response => {
                alert('Event created: ' + response.result.htmlLink);
            });
        }
    </script>



